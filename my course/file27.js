// more keyword/properties that we can use to deal with an array 

// we have push and pop 
// we have shif and unshift


let fruits =["apple","grape","mango","kiwi"];
console.log(fruits);


// ##########PUSH EXAMPLE###########
console.log("PUSH EXAMPLE");
// it pushes the data in last of an array 
fruits.push("banana");
console.log(fruits);

// ##########POP EXAMPLE########### 
console.log("POP EXAMPLE");
// it pops out the last data from an array 
fruits.pop();
console.log(fruits);

// ##########SHIFT EXAMPLE###########
console.log("SHIFT EXAMPLE");
// it pop the data from the start of array 
fruits.shift();
console.log(fruits);

// #########UNSHIFT EXAMPLE#########
console.log("UNSHIFT EXAMPLE");
// It adds data in the start of an array 
fruits.unshift("Orange");
console.log(fruits);